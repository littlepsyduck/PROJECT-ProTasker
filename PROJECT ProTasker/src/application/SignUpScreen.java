    private static boolean usernameExists(String username) {
        try (BufferedReader reader = new BufferedReader(new FileReader("src/data/account/account.txt"))) {
            String line;
            while ((line = reader.readLine()) != null) {
                String[] parts = line.split(",");
                if (parts.length >= 1 && parts[0].equals(username)) {
                    return true;
                }
            }
        } catch (IOException _) {

        }
        return false;
    }

    private static boolean registerUser(String username, String password) {
        try {
            try (FileWriter fileWriter = new FileWriter("src/data/account/account.txt", true)) {
                fileWriter.write(STR."\{username},\{password}\n");
            }
            return true;
        } catch (IOException e) {
            e.printStackTrace();
            return false;
        }
    }

