    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked
        // TODO add your handling code here:
        char[] pass1 = jPasswordField4.getPassword();
        char[] pass2 = signUpPassField2.getPassword();
        if (pass1.length != pass2.length) {
            jLabel2.setText("Passwords do not match!");
            signUpPassField2.setText("");
        } else {
            boolean match = true;
            for (int i = 0; i < pass1.length; i++) {
                if (pass1[i] != pass2[i]) {
                    match = false;
                    break;
                }
            }
            if (!match) {
                jLabel2.setText("Passwords do not match!");
                signUpPassField2.setText("");
            }
            else {
                jLabel2.setText("");
                String username = signUpNameField.getText();
                String password = new String(jPasswordField4.getPassword());
                if (username.isEmpty()) {
                    jLabel2.setText("Please enter a username.");
                } else if (password.isEmpty()) {
                    jLabel2.setText("Please enter a password.");
                } else if (usernameExists(username)) {
                    jLabel2.setText("<html> Username already exists! <br> Please choose another username.");
                } else {
                    if (registerUser(username, password)) {
                        Main.openCalendar(username);
                        //Main.openTaskManager(username);
                        dispose();
                    } else {
                        jLabel2.setText("Registration Failed!");
                    }
                }
            }
        }

    }//GEN-LAST:event_jLabel1MouseClicked

