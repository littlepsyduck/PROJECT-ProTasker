     void deleteTask() {
        int selectedRow = jTable3.getSelectedRow();
        loadTasksFromFile();
        DefaultTableModel model = (DefaultTableModel)jTable3.getModel();
        if (selectedRow != -1) {
            tasksAll.remove(selectedRow);
            model.removeRow(selectedRow);

            saveTasksToFile();
        }
    }

    static void saveTasksToFile() {
        String directoryPath = STR."src/data\{File.separator}\{username}.txt";
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(directoryPath))) {
            for (Task task : tasksAll) {
                writer.write(STR."\{task.id}~\{task.title}~\{task.description}~\{task.dueDate}~\{task.priority}~\{task.status}");
                writer.newLine();
            }
        } catch (IOException e) {
            e.getStackTrace();
        }
    }

