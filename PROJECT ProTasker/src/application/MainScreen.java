    public String getFullDate(int day, int month, int year) {
        Calendar cal = Calendar.getInstance();
        cal.set(year, month, day); // Set the date based on retrieved month, year and clicked day

        SimpleDateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        return dateFormat.format(cal.getTime());
    }

    static void updateMonth() {
        DefaultTableCellRenderer renderer = (DefaultTableCellRenderer) jTable1.getDefaultRenderer(Object.class);
        renderer.setHorizontalAlignment(SwingConstants.CENTER);
        cal.set(Calendar.DAY_OF_MONTH, 1);
        String month = cal.getDisplayName(Calendar.MONTH, Calendar.LONG, Locale.US);
        int year = cal.get(Calendar.YEAR);
        jLabel1.setText(month + " " + year);

        int startDay = cal.get(Calendar.DAY_OF_WEEK);
        int numberOfDays = cal.getActualMaximum(Calendar.DAY_OF_MONTH);


        int j = 0;
        for(int day=1;day<=42;day++){
            jTable1.setValueAt("", j/7 , j%7 );
            j = j + 1;
        }

        int i = startDay-1;
        for(int day=1;day<=numberOfDays;day++){
            jTable1.setValueAt(day, i/7 , i%7 );
            i = i + 1;
        }

    }

