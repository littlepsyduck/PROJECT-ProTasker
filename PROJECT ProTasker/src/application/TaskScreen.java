    /**
     * @param date the command line arguments
     */

    static boolean isValidDateFormat(String date) {
        DateFormat dateFormat = new SimpleDateFormat("dd/MM/yyyy");
        dateFormat.setLenient(true);
        try {
            dateFormat.parse(date);
            return true;
        } catch (Exception e){
            e.getStackTrace();
            return false;
        }
    }
    
