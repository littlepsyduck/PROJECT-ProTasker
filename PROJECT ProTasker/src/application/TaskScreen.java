    private void jLabel8MouseClicked(MouseEvent evt) {//GEN-FIRST:event_jLabel8MouseClicked
        // TODO add your handling code here:
        if(Objects.equals(action, "add")) {
            title = jTextField1.getText();
            description = jTextArea1.getText();
            dueDate = jTextField2.getText();
            priority = Objects.requireNonNull(jComboBox2.getSelectedItem()).toString();
            status = Objects.requireNonNull(jComboBox1.getSelectedItem()).toString();
            if (isValidDateFormat(dueDate)) {
                MainScreen.addTask();
                MainScreen.fullTasksFromFile();
                MainScreen.loadTasksFromFileWithDate();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid date format. Please enter the date in dd/mm/yyyy format.");
            }

        }
        else if (Objects.equals(action, "edit")) {
            title = jTextField1.getText();
            description = jTextArea1.getText();
            dueDate = jTextField2.getText();
            priority = Objects.requireNonNull(jComboBox2.getSelectedItem()).toString();
            status = Objects.requireNonNull(jComboBox1.getSelectedItem()).toString();
            if (isValidDateFormat(dueDate)) {
                MainScreen.editTask();
                MainScreen.fullTasksFromFile();
                MainScreen.loadTasksFromFileWithDate();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Invalid date format. Please enter the date in dd/mm/yyyy format.");
            }

        }

    }//GEN-LAST:event_jLabel8MouseClicked

